//! Manages CLIProxyAPI as a child process within Presenter.
//!
//! CLIProxyAPI is a Go binary that provides an OpenAI-compatible API by
//! authenticating with Claude via OAuth. Presenter bundles it and manages
//! its lifecycle automatically.

use std::path::{Path, PathBuf};
use std::sync::Arc;
use tokio::io::AsyncBufReadExt;
use tokio::process::{Child, Command};
use tokio::sync::RwLock;
use tracing::{info, warn};

/// Default port for the embedded CLIProxyAPI instance.
const DEFAULT_PROXY_PORT: u16 = 18787;

/// OAuth callback port used by CLIProxyAPI for Claude login.
const OAUTH_CALLBACK_PORT: u16 = 54545;

/// Name of the CLIProxyAPI binary.
const PROXY_BINARY_NAME: &str = "cli-proxy-api";

/// State of the managed CLIProxyAPI process.
#[derive(Debug, Clone, serde::Serialize)]
#[serde(rename_all = "camelCase")]
pub struct ProxyStatus {
    pub running: bool,
    pub port: u16,
    pub api_url: String,
    pub binary_found: bool,
    pub claude_authenticated: bool,
}

/// Configuration for the embedded proxy.
#[derive(Debug, Clone, serde::Serialize, serde::Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct ProxyConfig {
    #[serde(default = "default_proxy_port")]
    pub port: u16,
    #[serde(default = "default_true")]
    pub auto_start: bool,
}

fn default_proxy_port() -> u16 {
    DEFAULT_PROXY_PORT
}

fn default_true() -> bool {
    true
}

impl Default for ProxyConfig {
    fn default() -> Self {
        Self {
            port: DEFAULT_PROXY_PORT,
            auto_start: true,
        }
    }
}

/// Manages the CLIProxyAPI child process.
pub struct ProxyManager {
    child: Arc<RwLock<Option<Child>>>,
    login_child: Arc<RwLock<Option<Child>>>,
    config: Arc<RwLock<ProxyConfig>>,
    deploy_dir: PathBuf,
}

impl ProxyManager {
    pub fn new(deploy_dir: PathBuf) -> Self {
        Self {
            child: Arc::new(RwLock::new(None)),
            login_child: Arc::new(RwLock::new(None)),
            config: Arc::new(RwLock::new(ProxyConfig::default())),
            deploy_dir,
        }
    }

    /// Resolve the binary path. Searches:
    /// 1. Same directory as presenter-server (deploy dir)
    /// 2. Working directory
    /// 3. PATH
    fn binary_path(&self) -> Option<PathBuf> {
        let deploy_path = self.deploy_dir.join(PROXY_BINARY_NAME);
        if deploy_path.exists() {
            return Some(deploy_path);
        }

        let cwd_path = PathBuf::from(PROXY_BINARY_NAME);
        if cwd_path.exists() {
            return Some(cwd_path);
        }

        if let Ok(output) = std::process::Command::new("which")
            .arg(PROXY_BINARY_NAME)
            .output()
        {
            if output.status.success() {
                let path = String::from_utf8_lossy(&output.stdout).trim().to_string();
                if !path.is_empty() {
                    return Some(PathBuf::from(path));
                }
            }
        }

        None
    }

    /// Auth directory where CLIProxyAPI stores OAuth tokens.
    fn auth_dir(&self) -> PathBuf {
        self.deploy_dir.join(".cli-proxy-api")
    }

    /// Config file path for the managed instance.
    fn config_path(&self) -> PathBuf {
        self.deploy_dir.join("cli-proxy-api-config.yaml")
    }

    /// Check if Claude OAuth tokens exist.
    pub fn is_claude_authenticated(&self) -> bool {
        let auth_dir = self.auth_dir();
        auth_dir.exists()
            && std::fs::read_dir(&auth_dir)
                .map(|entries| {
                    entries
                        .filter_map(|e| e.ok())
                        .any(|e| e.file_name().to_string_lossy().contains("claude"))
                })
                .unwrap_or(false)
    }

    /// Write the config YAML file for CLIProxyAPI.
    async fn write_config(&self) -> anyhow::Result<()> {
        let config = self.config.read().await;
        let auth_dir = self.auth_dir();
        let config_content = format!(
            r#"# Auto-generated by Presenter - do not edit
host: "127.0.0.1"
port: {port}
auth-dir: "{auth_dir}"
debug: false
logging-to-file: false
request-retry: 2
"#,
            port = config.port,
            auth_dir = auth_dir.display(),
        );

        tokio::fs::create_dir_all(&auth_dir).await?;
        tokio::fs::write(self.config_path(), config_content).await?;
        Ok(())
    }

    /// Start the CLIProxyAPI process.
    pub async fn start(&self) -> anyhow::Result<()> {
        {
            let guard = self.child.read().await;
            if guard.is_some() {
                return Ok(());
            }
        }

        let binary = self
            .binary_path()
            .ok_or_else(|| anyhow::anyhow!("CLIProxyAPI binary not found"))?;

        self.write_config().await?;

        let config = self.config.read().await;
        info!(
            binary = %binary.display(),
            port = config.port,
            "starting CLIProxyAPI"
        );

        let child = Command::new(&binary)
            .arg("-config")
            .arg(self.config_path())
            .stdout(std::process::Stdio::null())
            .stderr(std::process::Stdio::piped())
            .kill_on_drop(true)
            .spawn()?;

        {
            let mut guard = self.child.write().await;
            *guard = Some(child);
        }

        // Wait briefly for it to start
        tokio::time::sleep(tokio::time::Duration::from_secs(2)).await;

        let port = config.port;
        drop(config);
        let url = format!("http://127.0.0.1:{port}/v1/models");
        match reqwest::Client::new()
            .get(&url)
            .timeout(std::time::Duration::from_secs(5))
            .send()
            .await
        {
            Ok(resp) if resp.status().is_success() => {
                info!(port, "CLIProxyAPI started successfully");
            }
            Ok(resp) => {
                warn!(port, status = %resp.status(), "CLIProxyAPI started but returned non-success");
            }
            Err(e) => {
                warn!(?e, "CLIProxyAPI may not have started correctly");
            }
        }
        Ok(())
    }

    /// Stop the CLIProxyAPI process.
    pub async fn stop(&self) -> anyhow::Result<()> {
        let mut guard = self.child.write().await;
        if let Some(mut child) = guard.take() {
            info!("stopping CLIProxyAPI");
            child.kill().await.ok();
            child.wait().await.ok();
        }
        Ok(())
    }

    /// Restart the process.
    #[allow(dead_code)]
    pub async fn restart(&self) -> anyhow::Result<()> {
        self.stop().await?;
        self.start().await
    }

    /// Get current status.
    pub async fn status(&self) -> ProxyStatus {
        let config = self.config.read().await;
        let port = config.port;
        drop(config);

        let binary_found = self.binary_path().is_some();
        let running = self.is_running().await;
        let claude_authenticated = self.is_claude_authenticated();

        ProxyStatus {
            running,
            port,
            api_url: format!("http://127.0.0.1:{port}/v1"),
            binary_found,
            claude_authenticated,
        }
    }

    /// Check if the process is still alive.
    async fn is_running(&self) -> bool {
        let mut guard = self.child.write().await;
        if let Some(ref mut child) = *guard {
            match child.try_wait() {
                Ok(None) => true,
                Ok(Some(_)) => {
                    *guard = None;
                    false
                }
                Err(_) => {
                    *guard = None;
                    false
                }
            }
        } else {
            false
        }
    }

    /// Start the Claude OAuth login flow.
    ///
    /// Spawns CLIProxyAPI with `--claude-login --no-browser`, reads the auth URL
    /// from its output, and rewrites the redirect_uri to point at Presenter's
    /// `/ai/oauth/callback` endpoint instead of `localhost:54545/callback`.
    ///
    /// `presenter_origin` should be the externally-reachable base URL,
    /// e.g. `http://10.77.8.134:8080`.
    ///
    /// Flow for the user:
    /// 1. Call this method → get the claude.ai auth URL (with Presenter as redirect)
    /// 2. Open URL in browser, authorize with Claude
    /// 3. Claude redirects to Presenter's /ai/oauth/callback?code=XXX&state=YYY
    /// 4. Presenter forwards the callback to CLIProxyAPI on localhost:54545
    /// 5. Done — no manual copy-paste needed
    pub async fn claude_login(&self, presenter_origin: &str) -> anyhow::Result<String> {
        // Kill any existing login process
        {
            let mut guard = self.login_child.write().await;
            if let Some(mut child) = guard.take() {
                child.kill().await.ok();
                child.wait().await.ok();
            }
        }

        let binary = self
            .binary_path()
            .ok_or_else(|| anyhow::anyhow!("CLIProxyAPI binary not found"))?;

        self.write_config().await?;

        info!("starting Claude OAuth login flow");

        let mut child = Command::new(&binary)
            .arg("-claude-login")
            .arg("-no-browser")
            .arg("-config")
            .arg(self.config_path())
            .stdout(std::process::Stdio::piped())
            .stderr(std::process::Stdio::null())
            .kill_on_drop(true)
            .spawn()?;

        // Read stdout line by line to find the auth URL
        let stdout = child
            .stdout
            .take()
            .ok_or_else(|| anyhow::anyhow!("failed to capture stdout"))?;

        let mut reader = tokio::io::BufReader::new(stdout).lines();
        let mut auth_url = None;

        // Read lines with a timeout — the URL should appear quickly
        let deadline = tokio::time::Instant::now() + tokio::time::Duration::from_secs(10);
        while tokio::time::Instant::now() < deadline {
            let line =
                tokio::time::timeout(tokio::time::Duration::from_secs(2), reader.next_line()).await;

            match line {
                Ok(Ok(Some(line))) => {
                    if line.contains("claude.ai/oauth/authorize") {
                        auth_url = Some(line.trim().to_string());
                        break;
                    }
                }
                Ok(Ok(None)) => break, // EOF
                Ok(Err(_)) => break,   // Read error
                Err(_) => continue,    // Timeout on this line, try next
            }
        }

        // Store the login child process — it needs to stay alive to receive the callback
        {
            let mut guard = self.login_child.write().await;
            *guard = Some(child);
        }

        let raw_url = auth_url
            .ok_or_else(|| anyhow::anyhow!("could not find auth URL in CLIProxyAPI output"))?;

        // Rewrite redirect_uri: replace localhost:54545 with Presenter's origin
        // so Claude redirects back to Presenter (which is reachable from the user's browser)
        let localhost_callback =
            format!("http%3A%2F%2Flocalhost%3A{OAUTH_CALLBACK_PORT}%2Fcallback");
        let presenter_callback = format!("{presenter_origin}/ai/oauth/callback")
            .replace(':', "%3A")
            .replace('/', "%2F");
        let rewritten = raw_url.replace(&localhost_callback, &presenter_callback);

        Ok(rewritten)
    }

    /// Complete the OAuth login by forwarding the callback URL to CLIProxyAPI.
    ///
    /// After the user authorizes in their browser, Claude redirects to
    /// `http://localhost:54545/callback?code=XXX&state=YYY`. Since the server
    /// is remote, this fails in the user's browser. The user copies the full
    /// URL from the browser error page and pastes it here.
    ///
    /// Presenter extracts the query parameters and calls CLIProxyAPI's
    /// callback endpoint on localhost.
    pub async fn complete_login(&self, callback_url: &str) -> anyhow::Result<()> {
        // Extract query string from the callback URL
        let query = if let Some(pos) = callback_url.find('?') {
            &callback_url[pos..]
        } else {
            anyhow::bail!("callback URL must contain query parameters (?code=...&state=...)");
        };

        // Forward to CLIProxyAPI's callback endpoint
        let target = format!("http://127.0.0.1:{OAUTH_CALLBACK_PORT}/callback{query}");
        info!("forwarding OAuth callback to CLIProxyAPI");

        // Don't follow redirects — CLIProxyAPI returns 302 on success
        let resp = reqwest::Client::builder()
            .redirect(reqwest::redirect::Policy::none())
            .build()?
            .get(&target)
            .timeout(std::time::Duration::from_secs(10))
            .send()
            .await?;

        let status = resp.status().as_u16();
        if status >= 400 {
            let body = resp.text().await.unwrap_or_default();
            anyhow::bail!("OAuth callback failed (HTTP {status}): {body}");
        }

        // Give CLIProxyAPI a moment to save tokens
        tokio::time::sleep(tokio::time::Duration::from_secs(2)).await;

        // Kill the login process — it's done
        {
            let mut guard = self.login_child.write().await;
            if let Some(mut child) = guard.take() {
                child.kill().await.ok();
                child.wait().await.ok();
            }
        }

        // Restart the main proxy to pick up the new credentials
        if self.is_running().await {
            info!("restarting CLIProxyAPI to use new credentials");
            self.stop().await?;
            self.start().await?;
        }

        info!("Claude OAuth login completed");
        Ok(())
    }

    /// Update the proxy configuration.
    #[allow(dead_code)]
    pub async fn update_config(&self, new_config: ProxyConfig) -> anyhow::Result<()> {
        let was_running = self.is_running().await;
        {
            let mut guard = self.config.write().await;
            *guard = new_config;
        }
        if was_running {
            self.restart().await?;
        }
        Ok(())
    }

    /// Get the API URL for this proxy instance.
    #[allow(dead_code)]
    pub async fn api_url(&self) -> String {
        let config = self.config.read().await;
        format!("http://127.0.0.1:{}/v1", config.port)
    }

    /// Auto-start if configured and binary exists.
    pub async fn auto_start(&self) {
        let config = self.config.read().await;
        let should_start = config.auto_start;
        drop(config);

        if should_start && self.binary_path().is_some() {
            if let Err(e) = self.start().await {
                warn!(?e, "failed to auto-start CLIProxyAPI");
            }
        }
    }
}

/// Determine the deploy directory (where presenter-server lives).
pub fn detect_deploy_dir() -> PathBuf {
    std::env::current_exe()
        .ok()
        .and_then(|p| p.parent().map(Path::to_path_buf))
        .unwrap_or_else(|| PathBuf::from("."))
}
